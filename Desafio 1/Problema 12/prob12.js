//12- Crie um loop que exiba todos os números pares de 0 a 20.
//No node.js
let numero = 0; // iniciar apartir de 0

while (numero <= 20) { //numeros ate 20
    if (numero % 2 == 0) { // número é par
        console.log(numero); // Exibe o número par
    }
    numero += 1; // Incrementa de 2 em 2
}
